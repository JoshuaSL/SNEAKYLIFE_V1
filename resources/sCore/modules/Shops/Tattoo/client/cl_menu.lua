function OpenTattooRageUIMenu()

    if Tatouage.Menu then
        Tatouage.Menu = false
    else
        Tatouage.Menu = true
        RageUI.Visible(RMenu:Get('tattoo', 'main'), true)

        Citizen.CreateThread(function()
			while Tatouage.Menu do
                FreezeEntityPosition(GetPlayerPed(-1), true)
                RageUI.IsVisible(RMenu:Get('tattoo', 'main'), true, false, true, function()
    
                    for k,v in pairs(TattooShop.TattooCategories) do
                        RageUI.Button(v.name, nil, { RightLabel = "" }, true, function()
                        end, RMenu:Get('tattoo', 'part'..k))
                    end
                    RageUI.Button("~r~Effacer tous mes tatouages", nil, { RightLabel = "~g~1500$ ~s~" }, not cleaning, function(Hovered, Active, Selected)
                        if (Selected) then
                            cleaning = true
                            SneakyEvent("SneakyLife:payClean")
                        end
                    end)
                end)
                for k,v in pairs(TattooShop.TattooCategories) do
                    RageUI.IsVisible(RMenu:Get('tattoo', 'part'..k), true, false, true, function()
                        RageUI.Button("~r~Supprimer la prvisualisation actuelle", nil, { RightLabel = "" }, true, function(Hovered, Active, Selected)
                            if (Selected) then
                                ClearPedDecorations(PlayerPedId())
                                for _,t in pairs(playerTatoos) do
                                    ApplyPedOverlay(PlayerPedId(),t.cat,t.name) 
                                end
                            end
                        end)
                        for index,tatoo in pairs(TattooShop.TattooList[v.value]) do
                            RageUI.Button("Tatouage n"..index, nil, { RightLabel = "~g~"..tatoo.price.."$ ~s~" }, true, function(Hovered, Active, Selected)
                                if (Active) then
                                    if currentPreviewTatoo ~= GetHashKey(tatoo.nameHash) then 
                                        ClearPedDecorations(PlayerPedId())
                                        for _,t in pairs(playerTatoos) do
                                            ApplyPedOverlay(PlayerPedId(),t.cat,t.name) 
                                        end
                                        ApplyPedOverlay(PlayerPedId(),GetHashKey(TattooShop.TattooCategories[k].value),GetHashKey(tatoo.nameHash)) 
                                    end
                                    currentPreviewTatoo = GetHashKey(tatoo.nameHash)
                                end
                                if (Selected) then
                                    local needModif = true
                                    for _,t in pairs(playerTatoos) do
                                        if GetHashKey(TattooShop.TattooCategories[k].value) == playerTatoos.cat and GetHashKey(tatoo.nameHash) == playerTatoos.name then
                                            needModif = false
                                        end
                                    end
                                    if needModif then
                                        table.insert(playerTatoos, {cat = GetHashKey(TattooShop.TattooCategories[k].value), name = GetHashKey(tatoo.nameHash)})
                                        SneakyEvent("SneakyLife:pay", tatoo.price, playerTatoos)
                                    else
                                        RageUI.Popup({message = "~r~Vous possdez dj ce tatouage!"})
                                    end
                                end
                            end)
                        end
                    end)
                end
				Wait(0)
			end
		end) 
	end
end

